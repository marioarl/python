'''
Escreva um programa que converta uma temperatura digitada em graus Celcius para Farenheit
'''
#minha resposta
temp = float(input('Informe a temperatura em C: '))
f = ((9 * temp) / 5) + 32
print('A temperatura em {}C corresponde a {}F !'. format(temp, f))


